#' Função diminui
#'
#'  Esta é uma função simples para diminuir dois números
#'
#'  @param a Primeiro número, pode ser inteiro ou double
#'  @param b Segundo número, pode ser inteiro ou double
#'
#'   @examples
#'   diminui(3,2)
#'   diminui(5,3)
#'
#'
#' @export
diminui <- function(a,b)
{
  a - b
}
